function legalPage(type) {
  const c = t();
  const privacy = type === "privacy";
  const sections = privacy ? c.legal.privacy : c.legal.terms;
  return `<main><div class="page-title"><div class="container"><span class="eyebrow">${c.legal.eyebrow}</span><h1>${privacy ? c.legal.privacyTitle : c.legal.termsTitle}</h1><p>${c.legal.updated}</p></div></div><section class="section"><article class="container legal">${sections.map(x=>`<h2>${x[0]}</h2><p>${x[1]}</p>`).join("")}</article></section></main>`;
}

function render() {
  syncDocumentMeta();
  captureTracking();
  const page = getPage();
  let content = homePage();
  if (page === "guest/marketing-plans") content = guestPage();
  else if (page === "user/services") content = servicesPage();
  else if (page === "user/faq") content = faqPage();
  else if (page === "about") content = aboutPage();
  else if (page === "contact") content = contactPage();
  else if (page === "user/auth/login") content = authPage("login");
  else if (page === "user/auth/signup") content = authPage("signup");
  else if (page === "user/auth/forgot-password") content = authPage("forgot");
  else if (page === "privacy") content = legalPage("privacy");
  else if (page === "terms") content = legalPage("terms");
  document.querySelector("#app").innerHTML = header() + content + footer();
  bindEvents();
}

function bindEvents() {
  document.querySelectorAll("[data-link]").forEach(el => el.addEventListener("click", e => {
    e.preventDefault();
    const url = new URL(el.href, location.origin);
    if (el.dataset.platform && itemsByPlatform()[el.dataset.platform]) {
      state.platform = el.dataset.platform; state.item = ""; state.plan = null; state.checkout = false; state.complete = false;
    }
    go(url.pathname + url.search + url.hash);
  }));
  document.querySelector("#languageToggle")?.addEventListener("click", () => {
    state.lang = state.lang === "en" ? "zh" : "en";
    localStorage.setItem("onlyfansone_lang", state.lang);
    state.item = "";
    render();
  });
  document.querySelector("#menuButton")?.addEventListener("click", () => document.querySelector("#navLinks").classList.toggle("open"));
  document.querySelectorAll("[data-select-platform]").forEach(el => el.addEventListener("click", () => { state.platform = el.dataset.selectPlatform; state.item = ""; state.plan = null; render(); }));
  document.querySelector("#itemSelect")?.addEventListener("change", e => { state.item = e.target.value; render(); });
  document.querySelectorAll("[data-plan]").forEach(el => el.addEventListener("click", () => { state.plan = plans[Number(el.dataset.plan)]; render(); }));
  document.querySelector("#continueBtn")?.addEventListener("click", () => { state.checkout = true; render(); window.scrollTo({top:0}); });
  document.querySelector("#checkoutForm")?.addEventListener("submit", e => { e.preventDefault(); state.complete = true; render(); window.scrollTo({top:0}); });
  document.querySelectorAll("[data-faq]").forEach(el => el.addEventListener("click", () => el.closest(".faq-item").classList.toggle("open")));
  document.querySelectorAll("[data-service-info]").forEach(el => el.addEventListener("click", () => toast(t().toast.service.replace("{id}", el.dataset.serviceInfo))));
  document.querySelector("#lineButton")?.addEventListener("click", () => toast(t().toast.line));
  document.querySelector("#authForm")?.addEventListener("submit", e => { e.preventDefault(); toast(t().toast.auth); });
  const search = document.querySelector("#serviceSearch");
  const filter = document.querySelector("#serviceFilter");
  const updateServices = () => {
    const term = search.value.toLowerCase();
    const p = filter.value;
    const allLabel = t().services.all;
    const rows = serviceRows().filter(r => (!term || `${r.category} ${r.name} ${r.id}`.toLowerCase().includes(term)) && (p === allLabel || r.name.includes(p)));
    document.querySelector("#serviceBody").innerHTML = serviceRowMarkup(rows);
    document.querySelectorAll("[data-service-info]").forEach(el => el.addEventListener("click", () => toast(t().toast.service.replace("{id}", el.dataset.serviceInfo))));
  };
  search?.addEventListener("input", updateServices);
  filter?.addEventListener("change", updateServices);
}

window.addEventListener("popstate", render);
window.addEventListener("hashchange", render);
render();
